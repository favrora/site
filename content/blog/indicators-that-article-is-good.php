    <h2>Step 6: Check the GPS</h2>

    <p>Use GPS to make sure you are heading in the right direction. Since the letters G, P, and S mean different things, do this step three times - once for G, one for P, and finally third for S.</p>

    <h3>G - grammar</h3>

    <p>Here are the pieces of grammar to check:

    <p>Are the sentences grammatically correct? Maybe you changed the sentence "Dr. Petrov speaks" to "American Medical Council experts ..." but forgot to change the word "speaks" to "speak."</p>

    <p>Are the parts of the sentence in place? When you talk about sleep: "In Paris at midnight he was already asleep." The sentence is out of order. It will be more understandable: "At midnight in Paris, he was already asleep."</p>

    <p>Are the times agreed? "Many people think they are not getting enough sleep and will decide to sleep more." Oh! The present has turned into the future, the proposal sounds strange. Here's the correct answer: "Many people think they don't get enough sleep and decide to sleep more."</p>

    <p>Prepositions in the right place? Prepositions describe the relationship of a noun with another word in a sentence. “Eat at the table” means eating while sitting at the table. “Eat on the table” sounds like nonsense. If you make a mistake in the preposition, the meaning will be lost.</p>

    <p>Using too many adjectives? Adjectives describe nouns: “Rejuvenating, restorative, immune-strengthening qualities of restful sleep ...” Adjectives like makeup. If used in moderation, it is beneficial. Overdo it and get the opposite effect.</p>

    <p>If there are many adjectives in the text, then the author was too lazy. Learn to convey thoughts without relying on adjectives.</p>

    <p>Using too many adverbs? Adverbs describe the verbs: "Eating quickly is harmful for several reasons." Adverbs are also indicative of laziness. Try to find the exact verbs instead: "Swallowing food is harmful for several reasons."</p>

    <p>Check the use of words. The phrase sounds normal when it is spoken in live speech: "Jumping off a cliff on a rope may not be a recreation option for everyone." But when writing, change the text to simple and understandable: "Jumping from a cliff with an elastic band is not a recreation option for everyone."</p>

    <p>Watch out for the words-parasites: "very", "real", "well", "so" and others. It is “very important” that you “really really love”, but it will make it difficult for the reader to understand the idea. These words are used so often that they have almost lost their meaning. Everyone has favorite words and phrases. Do you start a lot of sentences with "well"? Do you often use the words "only" and "even"? If you remove them, the post will benefit from this.</p>

    <h3>P - punctuation</h3>

    <ul>
      <li>Does each sentence end with a period, question, or exclamation mark?</li>
      <li>Does every sentence begin with a capital letter?</li>
      <li>Are the quotation marks placed correctly?</li>
      <li>Is there a single space between words and after each punctuation mark everywhere?</li>
      <li>Should you replace the comma with a dash or colon?</li>
    </ul>

    <h3>S - spelling</h3>

    <p>Automatic check will not help if you want to say about "lead poisoning", but write about "cotton poisoning". Check the spelling yourself. And make sure to spell the names of the people and titles correctly.</p>

    <p>You're done editing the post! GPS brought you to the end of your trip. But before you go off the road, see if you've done these three things:</p>

    <ul>
      <li>Have you read the plan?</li>
      <li>Keeping the audience in mind, focusing on what interests them?</li>
      <li>Did you follow these six steps in turn, checking only one thing at a time?</li>
      <li>If not, please go back and do it. You are close to the finish line - what to write the best post. It will be a shame to fail because of mistakes.</li>
    </ul>

    <p>After that, it remains only to click "Publish". Do it calmly. You gave this post everything you could. And it will return you a thousand times more when readers see it as a benefit and remember you as a good author.</p>


