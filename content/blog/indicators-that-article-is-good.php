    <p>Before publishing, the text still needs to be edited. It is easier to notice mistakes if you distract from the text for at least a day. But to eliminate roughness, check the text for different types of errors, and only one type at a time. And preliminarily put on the mask of the reader - the average representative of the audience. Constantly think about how to interest him. And follow these six steps in order.</p>

    <h2>Step 1: Check your balance</h2>

    <p>Are the introduction, body and conclusion balanced? Usually, the introduction and conclusion occupy no more than 10% of the post, and 90% will remain for the main part.</p>

    <p>Do all thoughts in the main body have equal weight? Our example is five things for a healthy lifestyle. So describe food, exercise, sleep, work, and rest in equal detail. If the importance of sleep has been confirmed by scientific research, find similar evidence for the rest of the elements of a healthy lifestyle.</p>


    <h2>Step 2: Check the paragraphs</h2>

    <p>Every time you see a new idea, make a new paragraph. Examples and stories can be in the same paragraphs or new. If you find the paragraph is too long, divide it by two or three. Vary the length of paragraphs to avoid visual and semantic monotony.</p>


    <h2>Step 3: Check the tone</h2>

    <p>Is the selected tone suitable for the theme? And for the audience? For a blog style? For example, you write about nutrition:</p>

    <ul>
      <li>Nuts are a source of healthy fats. They contain minerals that are essential for the functioning of the body. At the same time, they are more pleasant to eat than nutritional supplements. It makes sense to love nuts, right?</li>
      <li>The last sentence is the tone indicator. Change it depending on the audience. If you are writing for professionals over 40, but choose a professional and caring tone, the proposal will sound ridiculous. In this case, correct the tone or delete the sentence.</li>
    </ul>

    <p>The tone will help you connect with your audience. You can write:</p>

    <p>If you are not already eating nuts, add them to your diet. It sounds difficult, but it is not. To constantly eat nuts, it is enough to add them to the shopping list. Then you will not forget to buy them and start adding to dishes and eating a few pieces every day for a snack. It's that simple!</p>

    <p>Here, the tone helps to clarify the topic from the first sentence. It is a friendly tone that the author uses to show that he understands the reader's problems and knows how to help. Readers will immediately feel connected to the author and will want to know more.</p>

    <p>Re-read and think about the tone: tone it down or increase it, depending on what the audience wants.</p>


    <h2>Step 4. Read the post aloud</h2>

    <p>Read the post aloud slowly, clearly, and as loudly as possible. Mark what sounds wrong. When you finish reading, correct everything you noted.</p>


    <h2>Step 5: Relax</h2>

    <p>You have done a great job, time to rest. Please take a break from the text for at least a day. Forget about fasting and don't read, even for fun.</p>

