    <h3>Simplicity</h3>

    <p>Interesting fact: most users share an article before they finish reading it. In the course of a study conducted by Upworthy, it was found that reposts are made most often at the beginning of reading the material.</p>

    <p>This is best demonstrated by statistics: visitors who have read about 25% of the message are most willing to share it, but users who have read more than half of the post make a repost much less often. However, among those who read the entire article, there are also many who make a repost. This data is confirmed by the editor of Slon Magazine Maxim Kashulinsky.</p>

    <p>According to him, reposts of records are often made even by users who do not have the opportunity to read them to the end. It is logical to assume that many people share a post, paying attention to its title, introductory word and attached image. Therefore, the title should be simple, short, easy to understand, but well reflecting the essence of the article.</p>


    <h2>Where is the best place to place a repost button on your site</h2>

    <p>Imagine that it is located directly above the article. Then the user will have to read the same 25% of the text, go back and click on the button. This, of course, is not very convenient. So most won't do it. Someone will probably search for buttons at the very bottom of the article, but if they don't find them, they will leave the site and not make a repost.</p>

    <p>It is very easy to solve this problem:the buttons for the repost should be placed at the beginning of the material, and under the article, and, preferably, on the side, on the fixed panel. This will allow you to reach everyone: those who share the article after reading only the title, and those who hurry to repost the material, barely reaching half, and those who thoroughly read before telling their friends about the article.</p>


