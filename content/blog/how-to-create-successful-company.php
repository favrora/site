    <h2>Private investors</h2>

    <p>Rich people are one of the most promising sources of equity capital.  However, finding them is quite difficult.  Bankers, accountants, brokers, financial planners, and other entrepreneurs can often help.</p>


    <h2>Venture capitalists</h2>

    <p>In addition to seeking private investors, many fast-growing firms are making efforts to generate interest in their venture capitalists - investment professionals who raise capital to finance businesses that may succeed. The investment funds that venture capitalists have at their disposal are provided by corporations, wealthy people, pension funds, and other sources.</p>

    <p>Venture capitalists don't just lend to small businesses like banks do. They provide capital, while receiving a share of the property, which can reach 50% or more of the value of the entire enterprise.  They often assist in running the firm.  Typically, a venture capitalist purchases a portion of the company's shares at a low price, for example, 50 cents;  then when the company goes public, the venture capitalist sells them for a much higher price.</p>

    <p>One problem with venture capital is that it is very difficult to find. To get the attention of the typical venture capitalist, your business profile must demonstrate the potential to deliver 40-60% annual revenue growth in 5-7 years.</p>

    <p>If your business doesn't meet the requirements of venture capitalists, you can try getting money from investment firms. Small Business Investment Firms (ICMBs) work in the same way as venture capitalists, but they tend to invest in smaller "chunks" and pay attention to more modest firms.</p>

    <p>They operate under license from the federal authorities and are joint stock investment companies. These companies take out loans at a lower interest rate than conventional commercial loans in order to invest these funds in new businesses.</p>


    <h2>Corporate sources</h2>

    <p>Large corporations are another source of equity capital.  In addition, many large corporations provide equity capital through investment funds managed by venture capitalists.</p>

    <p>Generally speaking, the interests of large corporations are different from those of venture capitalists.  Most large companies are looking for more than just a return on their investments; they also want access to promising technologies and, possibly, acquire ownership of the firm. Corporate investors often provide more than just cash.  They sometimes share their marketing expertise or provide distribution channels that new businesses lack.</p>


    <h2>Sale of shares on the open market</h2>

    <p>If a rapidly growing company was able to survive for several years, it has the opportunity to increase its capital by corporatization (transformation into a public limited company), that is, by selling its shares on the open market.  This step leads to the achievement of two goals:</p>

    <ul>
      <li>1) attracts additional funds for the company;</li>
      <li>2) enables the founder and other original equity investors to make money by selling at a profit the shares they previously purchased at low prices.  While the potential gains from selling stocks on the open market can be enormous, there are difficulties as well.  Success depends on the demand of the population for shares of new companies, which changes significantly from year to year.</li>
    </ul>


    <h2>Planning the activities of the company</h2>

    <p>You can find many successful entrepreneurs who claim to have had little or no formal planning, but even those who rely mostly on their own intuition have at least a little thought about what they are trying to achieve and how they hope to do it.</p>

    <p>Before you rush into purchasing a product, you need to make sure that it finds a sale.  No matter how much you work, you will not be able to profit from a worthless idea.  A fish restaurant in an area where the diet consists mainly of potatoes, or a hotel in an area where mainly retirees live, are probably doomed to fail from the outset.</p>


    <h2>Franchise as a way to organize your business</h2>

    <p>One way to avoid the headaches when starting a new venture is to invest in a franchise, that is, a license that allows you to use the brand name of a larger company and sell its goods or services in a specific region.  For this right, the franchisee (licensee), usually the owner of a small firm, pays an initial fee to the franchisee (licensor), and subsequently pays a monthly fee.</p>

    <p>There are three main types of franchises.  If it is a SALE OF FINISHED GOODS franchise, the franchisee (licensee) pays the franchising corporation (licensor) for the right to sell products bearing its trademark.  The franchisee purchases these goods from the licensor and then resells them.  Car dealers and gas stations fall into this category.</p>

    <p>If it is a PRODUCTION goods franchise, the licensee receives from the parent company the right to manufacture and market its goods, and he uses the raw materials and materials purchased from that company.  An example is a soft drink bottling plant.</p>

    <p>If it is a franchise for a KIND OF ACTIVITY, the licensee buys the right to open his own company using the name of the licensor corporation and maintaining the profile of its activities.  The fast food chain is a typical example of this kind of franchise</p>


    <h2>Franchise advantages</h2>

    <p>Why is the franchise popular?  Using a franchise has a threefold benefit:</p>

    <ul>
      <li>â€œThe licensor wins because it lays a solid foundation for its company.</li>
      <li>The licensee wins because it can take advantage of the licensor's proven business practices.</li>
      <li>The general public benefits from the fact that it receives goods and services without interruption.</li>
    </ul>

    <p>Licensors tend to win the most, as they can expand their business through the licensees' outlets without affecting their own capital.  Licensors not only expand their business using other people's funds, but also receive regular income from licensees who pay them a certain percentage of their gross income and help pay for sales promotion and advertising costs.</p>

    <p>Investing capital in a franchise is also beneficial for the licensee, since the risk is relatively small.  By investing in a franchise, you know you are acquiring a viable business that has been successful for a long time.  You also have another advantage: people instantly recognize your firm's name, and you can count on massive publicity.</p>
