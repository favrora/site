    <h2>How internet marketing works</h2>

    <p>If you think with your own head, then you can come to the simplest conclusions. Internet marketing is not your entire business. This is only part of it. The same as hiring employees or motivating staff (there are several dozen other key tasks that you do as an entrepreneur or business manager).</p>

    <p>This is an important part, but it works towards a common goal. Which one? Well, let's discard false philosophizing here - naturally, to generate revenue. Or better yet, profit growth. In part, internet marketing can work for this task as well. But let's simplify our input for now in order to understand the principles of Internet marketing.</p>

    <p>Does Direct increase your revenue? It may or may not increase.</p>

    <p>Does SEO Increase Revenue? It may or may not increase.</p>


    <h2>Does social media promotion increase your revenue?</h2>

    <p>It may or may not increase. Why? Because this is only an opportunity to get customers, not the customers themselves. But the revenue is just them, your current customers. Take a piece of paper, write on it in large letters: my current clients. Now put an equal sign and add - revenue. You should have something like this: my current clients = revenue</p>

    <p>This is the simplest knowledge - remember it always. “My current customers” is a compound formula.</p>

    <p>Understood, huh? You don't need Direct, SEO or SMM. You need, sorry for the obviousness of the statement:</p>

    <ul>
      <li>either attracting new target users to the site;</li>
      <li>or improvement of the first level Conversion (increase in the number of leads);</li>
      <li>or improving the second level Conversion (from leads to customers);</li>
      <li>or an increase in the average check;</li>
      <li>or an increase in the returns of old customers;</li>
      <li>or improvement of all points at once (oh, how would you like it, huh?).</li>
    </ul>

    <p>What competencies should a hired agency or your own internet marketing department have to meet this challenge?</p>

    <ul>
      <li>the ability to attract search traffic, and this, in turn, requires competence in collecting semantics and compiling content for a broad semantic core;</li>
      <li>ability to work with context systems;</li>
      <li>Ability to work with various sites where your audience is present (it can be display advertising, teaser, article marketing - it doesn't matter);</li>
      <li>ability to work in social networks;</li>
      <li>competencies in email marketing (not to be confused with spam!), etc.</li>
    </ul>

    <h2>Average check of your business</h2>

    <p>The next key characteristic of a business is the average check. It doesn't matter what you do - services or selling goods, your business is b2b or b2c. This is an indicator that must be fixed and strive to increase.

    <p>How can you increase it? You, of course, concluded - well, of course, by raising the price of a service or product. Right? True, but only sometimes. In most cases, there is another possibility.</p>

    <p>If you sell products, then you can increase your average check through cross-selling. This is when a person comes to you, for example, to buy a mobile phone, and you immediately offer him to buy a cover and headphones for this product right away - just for this phone model, for a specific user. In services, this can be the sale of an additional service to the main one.</p>

    <p>But you need to understand that the sale of additional services should be based on the win-win principle. Your task is not just to sell an additional service, but to sell the one that the client needs - which can either enhance the effect of the first service, or help him in something else, which the client does not yet know about.</p>

    <p>Well, here's a trivial example: when a client contacts us to order a landing page, we also try to sell him A / B testing. But not because it is profitable for us, because everyone will benefit from this - the price for ordering a landing page and A / B testing will be lower than an order separately: having received the results of A / B testing, the client ultimately receives the most conversion landing page from several possible.</p>


    <h2>Customer returns</h2>

    <p>Internet marketers can solve this problem as well. Here, the key competencies are conducting remarketing / retargeting campaigns, maintaining email campaigns for customers, creating new content on the site, etc. At the same time, both mailing and new content should provide some additional value for your customers, in addition to informing them about new arrivals goods.</p>

    <p>What is described above is an internet marketing sales funnel. “Oh, how simple, I have read about it so many times in various clever books,” you say. Yes, everything is very simple. Elementary. But, damn it, how few people implement even the control of these indicators, not to mention the purposeful work on them. Are you doing well with internet marketing? Exactly? Are you sure? Security question: do you know your site's metrics for each item? The answer, "in general, yes" does not mean anything. You should have numbers, specific numbers: traffic - digit, conversion 1 - digit (percentage), conversion 2 - digit (percentage), new customers - digit, average check - digit, refunds - digit.</p>

    <p>Where do internet marketing failures come from?</p>

    <p>Now let's get back to where we started. Unfortunately, in the overwhelming majority of cases, the standard customer of the services of an agency specializing in attracting traffic does not understand what he really needs. He has a feeling that by ordering promotion on RF requests, he will receive coverage. He has a feeling that by ordering Yandex.Direct, he will receive leads. And so on, and so on. Sometimes it works. But more often than not.</p>

    <p>Do you think I'm talking about some kind of detached customer? No, dear reader, I'm talking about you. Unfortunately, in the overwhelming majority of cases, the customer, the business owner, cannot determine exactly what he really needs. You may be an exception. It's easy to check: ask yourself the question - are you monitoring the key metrics of your internet marketing? If so, then with a high degree of probability you really know what you need. But if not, you can only guess about it.</p>


